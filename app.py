# ---- Model ----
# –û–ø—Ä–µ–¥–µ–ª—è–µ–º —Ç—É –∂–µ –∞—Ä—Ö–∏—Ç–µ–∫—Ç—É—Ä—É –Ω–µ–π—Ä–æ–Ω–Ω–æ–π —Å–µ—Ç–∏, —á—Ç–æ –∏ –ø—Ä–∏ –æ–±—É—á–µ–Ω–∏–∏
class SmallCNN(nn.Module):
    def __init__(self):
        super().__init__()
        self.net = nn.Sequential(
            nn.Conv2d(1, 16, 3, padding=1), nn.ReLU(), nn.MaxPool2d(2),
            nn.Conv2d(16, 32, 3, padding=1), nn.ReLU(), nn.MaxPool2d(2),
            nn.Flatten(),
            nn.Linear(32 * 7 * 7, 128), nn.ReLU(),
            nn.Linear(128, 10)
        )

    def forward(self, x):
        return self.net(x)


# –î–µ–∫–æ—Ä–∞—Ç–æ—Ä @st.cache_resource –∫—ç—à–∏—Ä—É–µ—Ç –∑–∞–≥—Ä—É–∑–∫—É –º–æ–¥–µ–ª–∏
# –≠—Ç–æ –ø—Ä–µ–¥–æ—Ç–≤—Ä–∞—â–∞–µ—Ç –ø–æ–≤—Ç–æ—Ä–Ω—É—é –∑–∞–≥—Ä—É–∑–∫—É –º–æ–¥–µ–ª–∏ –ø—Ä–∏ –∫–∞–∂–¥–æ–º –≤–∑–∞–∏–º–æ–¥–µ–π—Å—Ç–≤–∏–∏ —Å –ø—Ä–∏–ª–æ–∂–µ–Ω–∏–µ–º
@st.cache_resource
def load_model():
    # –ó–∞–≥—Ä—É–∂–∞–µ–º —Å–æ—Ö—Ä–∞–Ω–µ–Ω–Ω—ã–µ –≤–µ—Å–∞ –º–æ–¥–µ–ª–∏
    # map_location="cpu" –≥–∞—Ä–∞–Ω—Ç–∏—Ä—É–µ—Ç –∑–∞–≥—Ä—É–∑–∫—É –Ω–∞ CPU –¥–∞–∂–µ –µ—Å–ª–∏ –æ–±—É—á–µ–Ω–∏–µ –±—ã–ª–æ –Ω–∞ GPU
    ckpt = torch.load("models/mnist_cnn.pth", map_location="cpu")
    m = SmallCNN()
    # –ó–∞–≥—Ä—É–∂–∞–µ–º –≤–µ—Å–∞ –∏–∑ checkpoint'–∞
    m.load_state_dict(ckpt["state_dict"])
    # –ü–µ—Ä–µ–≤–æ–¥–∏–º –º–æ–¥–µ–ª—å –≤ —Ä–µ–∂–∏–º –æ—Ü–µ–Ω–∫–∏ (–æ—Ç–∫–ª—é—á–∞–µ—Ç dropout, batch norm –≤ inference mode)
    m.eval()
    return m


# –ó–∞–≥—Ä—É–∂–∞–µ–º –º–æ–¥–µ–ª—å –ø—Ä–∏ —Å—Ç–∞—Ä—Ç–µ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è
model = load_model()


# ---- DB helpers ----
def fetch_one_per_digit():
    """
    –ü–æ–ª—É—á–∞–µ—Ç –∏–∑ –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö –ø–æ –æ–¥–Ω–æ–º—É —Å–ª—É—á–∞–π–Ω–æ–º—É –ø—Ä–∏–º–µ—Ä—É –¥–ª—è –∫–∞–∂–¥–æ–π —Ü–∏—Ñ—Ä—ã (0-9)
    –∏–∑ —Ç–µ—Å—Ç–æ–≤–æ–≥–æ –Ω–∞–±–æ—Ä–∞ –¥–∞–Ω–Ω—ã—Ö.

    Returns:
        list: –°–ø–∏—Å–æ–∫ –∏–∑ 10 —Å—Ç—Ä–æ–∫, –∫–∞–∂–¥–∞—è —Å–æ–¥–µ—Ä–∂–∏—Ç –¥–∞–Ω–Ω—ã–µ –æ–± –æ–¥–Ω–æ–º –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–∏
    """
    conn = get_conn()
    try:
        out = []
        with conn.cursor() as cur:
            # –î–ª—è –∫–∞–∂–¥–æ–π —Ü–∏—Ñ—Ä—ã –æ—Ç 0 –¥–æ 9
            for d in range(10):
                # SQL –∑–∞–ø—Ä–æ—Å –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è –æ–¥–Ω–æ–≥–æ —Å–ª—É—á–∞–π–Ω–æ–≥–æ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è
                cur.execute("""
                    SELECT id,label,vec,rows,cols
                    FROM demo.mnist_samples
                    WHERE split='test' AND label=%s
                    ORDER BY random() LIMIT 1
                """, (d,))
                row = cur.fetchone()
                out.append(row)
        return out
    finally:
        # –í—Å–µ–≥–¥–∞ –≤–æ–∑–≤—Ä–∞—â–∞–µ–º —Å–æ–µ–¥–∏–Ω–µ–Ω–∏–µ –≤ –ø—É–ª
        put_conn(conn)


def vec_to_pil(vec_blob, rows, cols):
    """
    –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ—Ç –±–∏–Ω–∞—Ä–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è –∏–∑ –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö –≤ –æ–±—ä–µ–∫—Ç PIL Image.

    Args:
        vec_blob: –±–∏–Ω–∞—Ä–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è –∏–∑ –∫–æ–ª–æ–Ω–∫–∏ vec
        rows: –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ç—Ä–æ–∫ –≤ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–∏
        cols: –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ç–æ–ª–±—Ü–æ–≤ –≤ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–∏

    Returns:
        PIL.Image: –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ –≤ –≥—Ä–∞–¥–∞—Ü–∏—è—Ö —Å–µ—Ä–æ–≥–æ
    """
    # –í–æ—Å—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –º–∞—Å—Å–∏–≤ –∏–∑ –±–∏–Ω–∞—Ä–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
    # dtype=np.float32 - —Ç–∞–∫ –∫–∞–∫ –¥–∞–Ω–Ω—ã–µ —Å–æ—Ö—Ä–∞–Ω—è–ª–∏—Å—å –∫–∞–∫ float32
    arr = np.frombuffer(vec_blob, dtype=np.float32).reshape(rows, cols)  # –∑–Ω–∞—á–µ–Ω–∏—è –≤ [0..1]
    # –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –≤ uint8 [0..255] –∏ —Å–æ–∑–¥–∞–µ–º PIL Image
    return Image.fromarray((arr * 255).astype(np.uint8))


def predict_pil(imgL_28):
    """
    –í—ã–ø–æ–ª–Ω—è–µ—Ç –ø—Ä–µ–¥—Å–∫–∞–∑–∞–Ω–∏–µ —Ü–∏—Ñ—Ä—ã –Ω–∞ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–∏ —Å –ø–æ–º–æ—â—å—é –Ω–µ–π—Ä–æ–Ω–Ω–æ–π —Å–µ—Ç–∏.

    Args:
        imgL_28: PIL Image —Ä–∞–∑–º–µ—Ä–æ–º 28x28 –≤ –≥—Ä–∞–¥–∞—Ü–∏—è—Ö —Å–µ—Ä–æ–≥–æ

    Returns:
        tuple: (–ø—Ä–µ–¥—Å–∫–∞–∑–∞–Ω–Ω–∞—è —Ü–∏—Ñ—Ä–∞, –º–∞—Å—Å–∏–≤ –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç–µ–π –¥–ª—è –≤—Å–µ—Ö —Ü–∏—Ñ—Ä)
    """

    # –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º PIL Image –≤ numpy array –∏ –Ω–æ—Ä–º–∞–ª–∏–∑—É–µ–º –≤ [0, 1]
    img_array = np.array(imgL_28, dtype=np.float32) / 255.0

    # –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –≤ —Ç–µ–Ω–∑–æ—Ä PyTorch –∏ –¥–æ–±–∞–≤–ª—è–µ–º —Ä–∞–∑–º–µ—Ä–Ω–æ—Å—Ç–∏ –±–∞—Ç—á–∞ –∏ –∫–∞–Ω–∞–ª–∞
    # unsqueeze(0) - –¥–æ–±–∞–≤–ª—è–µ—Ç —Ä–∞–∑–º–µ—Ä–Ω–æ—Å—Ç—å –±–∞—Ç—á–∞: [28,28] -> [1,28,28]
    # unsqueeze(0) - –¥–æ–±–∞–≤–ª—è–µ—Ç —Ä–∞–∑–º–µ—Ä–Ω–æ—Å—Ç—å –∫–∞–Ω–∞–ª–∞: [1,28,28] -> [1,1,28,28]
    x = torch.from_numpy(img_array).unsqueeze(0).unsqueeze(0)  # [1,1,28,28]

    # –û—Ç–∫–ª—é—á–∞–µ–º –≤—ã—á–∏—Å–ª–µ–Ω–∏–µ –≥—Ä–∞–¥–∏–µ–Ω—Ç–æ–≤ –¥–ª—è —É—Å–∫–æ—Ä–µ–Ω–∏—è –∏ —ç–∫–æ–Ω–æ–º–∏–∏ –ø–∞–º—è—Ç–∏
    with torch.no_grad():
        # –ü—Ä—è–º–æ–π –ø—Ä–æ—Ö–æ–¥ —á–µ—Ä–µ–∑ –º–æ–¥–µ–ª—å
        logits = model(x)  # –ø–æ–ª—É—á–∞–µ–º –ª–æ–≥–∏—Ç—ã [1,10]

        # –ü—Ä–∏–º–µ–Ω—è–µ–º softmax –¥–ª—è –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏—è –ª–æ–≥–∏—Ç–æ–≤ –≤ –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç–∏
        probs = F.softmax(logits, dim=1).squeeze(0).numpy()  # [10]

    # –í–æ–∑–≤—Ä–∞—â–∞–µ–º —Ü–∏—Ñ—Ä—É —Å –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–π –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å—é –∏ –≤—Å–µ –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç–∏
    return int(np.argmax(probs)), probs


