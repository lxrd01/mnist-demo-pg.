# –ò—Å–ø–æ–ª—å–∑—É–µ–º session_state –¥–ª—è —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏—è —Å–æ—Å—Ç–æ—è–Ω–∏—è –º–µ–∂–¥—É –ø–µ—Ä–µ–∑–∞–≥—Ä—É–∑–∫–∞–º–∏
# –ï—Å–ª–∏ –∫–Ω–æ–ø–∫–∞ –Ω–∞–∂–∞—Ç–∞ –∏–ª–∏ grid –µ—â–µ –Ω–µ –∑–∞–≥—Ä—É–∂–µ–Ω, –∑–∞–≥—Ä—É–∂–∞–µ–º –Ω–æ–≤—ã–µ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è
if "grid" not in st.session_state or st.button("üîÑ –û–±–Ω–æ–≤–∏—Ç—å —Ç–µ—Å—Ç–æ–≤—ã–µ –∫–∞—Ä—Ç–∏–Ω–∫–∏"):
    st.session_state.grid = fetch_one_per_digit()

# –°–æ–∑–¥–∞–µ–º 10 –∫–æ–ª–æ–Ω–æ–∫ –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è —Ü–∏—Ñ—Ä 0-9
cols = st.columns(10, gap="small")
clicked = None  # –ø–µ—Ä–µ–º–µ–Ω–Ω–∞—è –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è –≤—ã–±—Ä–∞–Ω–Ω–æ–≥–æ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è

# –û—Ç–æ–±—Ä–∞–∂–∞–µ–º –ø–æ –æ–¥–Ω–æ–º—É –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—é –¥–ª—è –∫–∞–∂–¥–æ–π —Ü–∏—Ñ—Ä—ã
for i, col in enumerate(cols):
    # –ï—Å–ª–∏ –¥–ª—è —ç—Ç–æ–π —Ü–∏—Ñ—Ä—ã –Ω–µ—Ç –¥–∞–Ω–Ω—ã—Ö –≤ –ë–î
    if st.session_state.grid[i] is None:
        col.write("‚Äî")  # –ø–æ–∫–∞–∑—ã–≤–∞–µ–º –ø—Ä–æ—á–µ—Ä–∫
        continue

    # –†–∞—Å–ø–∞–∫–æ–≤—ã–≤–∞–µ–º –¥–∞–Ω–Ω—ã–µ –∏–∑ —Å—Ç—Ä–æ–∫–∏ –ë–î
    _id, lab, vec, r, c = st.session_state.grid[i]
    # –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –±–∏–Ω–∞—Ä–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ –≤ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ
    img = vec_to_pil(vec, r, c)
    # –û—Ç–æ–±—Ä–∞–∂–∞–µ–º –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ —Å –ø–æ–¥–ø–∏—Å—å—é (—Ü–∏—Ñ—Ä–∞)
    col.image(img, caption=str(lab), use_container_width=True)
    # –°–æ–∑–¥–∞–µ–º –∫–Ω–æ–ø–∫—É –¥–ª—è –≤—ã–±–æ—Ä–∞ —ç—Ç–æ–≥–æ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è
    if col.button(f"–í—ã–±—Ä–∞—Ç—å {lab}", key=f"pick_{_id}"):
        # –ü—Ä–∏ –Ω–∞–∂–∞—Ç–∏–∏ –∫–Ω–æ–ø–∫–∏ –¥–µ–ª–∞–µ–º –ø—Ä–µ–¥—Å–∫–∞–∑–∞–Ω–∏–µ
        pred, probs = predict_pil(img)
        clicked = (img, pred, probs)

# –ï—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –≤—ã–±—Ä–∞–ª –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ, –ø–æ–∫–∞–∑—ã–≤–∞–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã
if clicked:
    img, pred, probs = clicked
    st.success(f"–ü—Ä–µ–¥—Å–∫–∞–∑–∞–Ω–∏–µ: **{pred}**")
    # –û—Ç–æ–±—Ä–∞–∂–∞–µ–º —Å—Ç–æ–ª–±—á–∞—Ç—É—é –¥–∏–∞–≥—Ä–∞–º–º—É –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç–µ–π –¥–ª—è –≤—Å–µ—Ö —Ü–∏—Ñ—Ä
    st.bar_chart({str(i): probs[i] for i in range(10)})

